add column if not exists subscription_tier text default 'free';

-- 2. PvP Photo Challenge
create table challenges (
  id bigint generated by default as identity primary key,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  theme text not null,
  start_date timestamp with time zone not null,
  end_date timestamp with time zone not null,
  status text default 'active' -- active, completed
);

create table challenge_entries (
  id bigint generated by default as identity primary key,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  challenge_id bigint references challenges(id) not null,
  user_id uuid references auth.users not null,
  image_url text not null,
  votes integer default 0,
  
  unique(challenge_id, user_id) -- One entry per user per challenge
);

-- RLS for Challenges
alter table challenges enable row level security;
create policy "Challenges are viewable by everyone." on challenges for select using (true);

-- RLS for Entries
alter table challenge_entries enable row level security;
create policy "Entries are viewable by everyone." on challenge_entries for select using (true);
create policy "Users can insert their own entries." on challenge_entries for insert with check (auth.uid() = user_id);
create policy "Users can update their own entries." on challenge_entries for update using (auth.uid() = user_id);

-- 3. Collab Finder (Add columns to profiles)
alter table profiles
