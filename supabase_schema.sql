add column if not exists bio text,
add column if not exists looking_for_collab boolean default false,
add column if not exists contact_email text; -- For the "Connect" button

-- Create an index for faster collab searching
create index if not exists profiles_niche_idx on profiles(niche);


-- 4. Guest Usage Tracking
create table if not exists guest_usage (
  id bigint generated by default as identity primary key,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  ip_address text not null,
  usage_date date not null,
  count integer default 0,
  
  unique(ip_address, usage_date)
);

-- RLS for Guest Usage (Allow public access for IP tracking)
alter table guest_usage enable row level security;

create policy "Enable read access for all users"
on guest_usage for select
using (true);

create policy "Enable insert for all users"
on guest_usage for insert
with check (true);

create policy "Enable update for all users"
on guest_usage for update
using (true);

-- 5. Connected Accounts (Social Media Integration)
create table if not exists connected_accounts (
  id bigint generated by default as identity primary key,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  user_id uuid references auth.users not null,
